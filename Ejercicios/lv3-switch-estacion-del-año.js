"use strict";
/* Usa switch para mostrar la estación según el número del mes. */
let estacion = "primavera";
switch (estacion) {
    case "primavera":
        console.log("es primavera");
        break;
    case "verano":
        console.log("es verano");
        break;
    case "otoño":
        console.log("es otoño");
        break;
    case "invierno":
        console.log("es invierno");
    default:
        console.log("es un dia normal");
        break;
}
